- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSString *CellIdentifier = @"Cell_TYPE_FIRST";
	if ([indexPath section] == 1) {
		CellIdentifier = @"Cell_TYPE_SECOND";
	}
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
	[cell.textLabel setText:[NSString stringWithFormat:@"Text_%d_%d", [indexPath section], [indexPath row]]];
	[cell.detailTextLabel setText:[NSString stringWithFormat:@"DetailText_%d_%d", [indexPath section], [indexPath row]]];
    
    return cell;
}

- (NSString *)tableView:(UITableView *)tableView titleForHeaderInSection:(NSInteger)section
{
	return [NSString stringWithFormat:@"Section Header %d", section];
}

- (NSString *)tableView:(UITableView *)tableView titleForFooterInSection:(NSInteger)section
{
	return [NSString stringWithFormat:@"Section footer %d", section];
}

